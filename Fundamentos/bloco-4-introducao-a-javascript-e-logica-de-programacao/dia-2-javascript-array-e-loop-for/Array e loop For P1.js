
let pizzas = [' 4 quejos', ' Frango com Catupiry', ' Maguerita', ' Palmito', ' Chocolate'];
pizzas[5] = ' Peito de Peru'; // Adiciona o novo sabor a posição 5 do Array
pizzas.push(' Peito de Avestruz'); // Adiciona o novo sabor na ultima posição do Array

console.log(pizzas); // Exibe todos os itens do Array
console.log(pizzas[0]); // Exibe o Item do Array que tá na primeira posição
console.log(pizzas.length); // Exibe o comprimento do Array, ou seja, a quantidades de itens do Array
console.log(pizzas.sort()); // Exibe todos os itens do Array em ordem alfabetica

